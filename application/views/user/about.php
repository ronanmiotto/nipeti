          <li>Executar e participar da execução, dentro de sua competência, de projetos de pesquisa e desenvolvimento;</li>
          <li>Estimular e orientar a participação de alunos em projetos de pesquisa e desenvolvimento;</li>
          <li>Estimular a pesquisa em áreas que ofereçam perspectiva de atendimento às necessidades da região e, de modo geral, do país;</li>
          <li>Agir, através da intermediação de contratos, convênios e acordos, entre a universidade e entidades públicas e privadas, e entre estas e pesquisadores.</li><br>
