        <p align="justify">
          O NIPETI é mantido por professores e alunos da área de Informática do IFMS (Instituto Federal de Educação, Ciência e Tecnologia de Mato Grosso do Sul), tendo como objetivo geral promover uma integração das disciplinas dos cursos da referida área, unindo teoria e prática e permitindo uma colaboração entre alunos e professores da instituição.
          Quanto ao ensino, o NIPETI visa integrar os conceitos teóricos trabalhados nas disciplinas dos cursos de Ensino Médio Integrado Técnico em Informática (matutino e vespertino) e Técnico em Manutenção e Suporte em Informática (noturno), com as metodologias aplicadas no mercado, provendo um elo ativo entre pesquisa e prática.
